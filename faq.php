    <p> <b> 5- O que é bug? </b> <br>
        Falha ou erro no código de um programa que provoca seu mau funcionamento. </p>

    <br>
    <p> <b> 6- Por que fazer backup? </b> <br>
        O backup existe para auxiliar na prevenção de perda de dados, como arquivos apagados acidentalmente por falha física ou humana. Caso o sistema seja pequeno, pode-se considerar um método pequeno, de backups 'full', ou seja, que vários dados sejam copiados todos os dias. O inconveniente é a demora desse tipo de backup.</p>

    <br>
    <p> <b> 7- O que é um upgrade? </b> <br>
        Atualização dos componentes do hardware ou do software, de um computador.</p>

    <br>
    <p> <b> 8- O que é Correio Eletrônico?</b> <br>
        O correio eletrônico ou e-mail é uma ferramenta usada para enviar e receber mensagens de maneira instantânea através da Internet.
    </p>
    <br>

    <p> <b> 9- O que é hardware? </b> <br>
        O hardware é a parte que você pode ver do computador, ou seja, todos os componentes da sua estrutura física. A tela, o teclado, o gabinete e o mouse fazem parte do hardware do seu computador.
    </p>
    <br>

    <p> <b> 10- O que é software? </b> <br>
        São os programas que permitem realizar atividades específicas em um computador. Por exemplo, os programas como Word, Excel, Power Point, os navegadores, os jogos, os sistemas operacionais, entre outros. </p>







